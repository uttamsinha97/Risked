ORG 0H
	SJMP 30H
ORG 30H
	MOV R1,#04H	   //ARRAY SIZE
	MOV @R0,30H	//ARRAY ELEMENTS		
SUM:MOV B,#02H
	MOV A,#00
	MOV A,@R0
	DIV AB
	MOV A,B
	JZ EVEN			   //JUMP TO EVEN WHEN A=0
	JNZ ODD			  //JUMP TO ODD WHEN A!=0
EVEN: MOV A,@R0
	 ADD A,R4
	 MOV R4,A
	 MOV 40H,R4	   //SUM OF EVEN 
	 INC R0
	 DJNZ R1,SUM
ODD:MOV A,@R0
	 ADD A,R5
	 MOV R5,A
	 MOV 41H,R5			//SUM OF ODD
	 INC R0
	 DJNZ R1,SUM
LP:SJMP LP
	END